test('BLA-57 requested numeric fields accept decimals while animal counts remain whole numbers', () => {
    assert.doesNotThrow(() => validate({
        mortalityCount: '12.5',
        revenue: '305218.90',
        farmImplementUsedHours: '40.5',
        farmImplementUsedInHours: '5.5',
        revenueGeneratedRs: '2050.25',
        revenueGeneratedByFarmImplement: '50.50',
    }));

    assert.throws(
        () => validate({ animalsTreated: '12.5' }),
        /Field "animalsTreated" must be a whole number/,
    );
});

