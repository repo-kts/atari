test('BLA-57 NICRA Revenue Generated supports decimal create, edit, and view', async () => {
    const originalCreate = prisma.nicraRevenueGenerated.create;
    const originalFindFirst = prisma.nicraRevenueGenerated.findFirst;
    const originalUpdate = prisma.nicraRevenueGenerated.update;
    let stored = {
        nicraRevenueGeneratedId: 1,
        kvkId: 1,
        reportingYear: new Date('2026-01-01T00:00:00.000Z'),
        revenue: 305218.9,
    };

    prisma.nicraRevenueGenerated.create = async ({ data }) => {
        stored = { ...stored, ...data };
        return stored;
    };
    prisma.nicraRevenueGenerated.findFirst = async () => stored;
    prisma.nicraRevenueGenerated.update = async ({ data }) => {
        stored = { ...stored, ...data };
        return stored;
    };

    try {
        const created = await nicraRevenueRepository.create({
            kvkId: 1,
            reportingYear: '2026-01-01',
            revenue: '305218.90',
        });
        const updated = await nicraRevenueRepository.update(1, { revenue: '12500.75' });
        const viewed = await nicraRevenueRepository.findById(1);

        assert.equal(created.revenue, 305218.9);
        assert.equal(updated.revenue, 12500.75);
        assert.equal(viewed.revenue, 12500.75);
    } finally {
        prisma.nicraRevenueGenerated.create = originalCreate;
        prisma.nicraRevenueGenerated.findFirst = originalFindFirst;
        prisma.nicraRevenueGenerated.update = originalUpdate;
    }
});

test('BLA-57 NICRA Custom Hiring supports decimal create, edit, and view', async () => {
    const originalCreate = prisma.nicraFarmImplement.create;
    const originalFindFirst = prisma.nicraFarmImplement.findFirst;
    const originalUpdate = prisma.nicraFarmImplement.update;
    let stored = farmImplementRecord();

    prisma.nicraFarmImplement.create = async ({ data }) => {
        stored = farmImplementRecord(data);
        return stored;
    };
    prisma.nicraFarmImplement.findFirst = async () => stored;
    prisma.nicraFarmImplement.update = async ({ data }) => {
        stored = { ...stored, ...data };
        return stored;
    };

    try {
        const created = await nicraFarmImplementRepository.create({
            kvkId: 1,
            reportingYear: '2026-01-01',
            startDate: '2026-01-01',
            endDate: '2026-01-02',
            nameOfFarmImplement: 'Seed drill',
            farmImplementUsedHours: '40.5',
            revenueGeneratedRs: '2050.25',
        });
        const updated = await nicraFarmImplementRepository.update(1, {
            farmImplementUsedHours: '5.5',
            revenueGeneratedRs: '50.50',
        });
        const viewed = await nicraFarmImplementRepository.findById(1);

        assert.equal(created.farmImplementUsedHours, 40.5);
        assert.equal(created.revenueGeneratedRs, 2050.25);
        assert.equal(updated.farmImplementUsedHours, 5.5);
        assert.equal(updated.revenueGeneratedRs, 50.5);
        assert.equal(viewed.farmImplementUsedInHours, 5.5);
        assert.equal(viewed.revenueGeneratedByFarmImplement, 50.5);
    } finally {
        prisma.nicraFarmImplement.create = originalCreate;
        prisma.nicraFarmImplement.findFirst = originalFindFirst;
        prisma.nicraFarmImplement.update = originalUpdate;
    }
});

test('BLA-57 NICRA report data and Custom Hiring report preserve decimals', async () => {
    const originalRevenueFindMany = prisma.nicraRevenueGenerated.findMany;
    const originalFarmFindMany = prisma.nicraFarmImplement.findMany;

    prisma.nicraRevenueGenerated.findMany = async () => [{
        nicraRevenueGeneratedId: 1,
        kvkId: 1,
        revenue: 12500.75,
        kvk: { kvkName: 'Test KVK', state: { stateName: 'Bihar' } },
    }];
    prisma.nicraFarmImplement.findMany = async () => [farmImplementRecord({
        farmImplementUsedHours: 5.5,
        revenueGeneratedRs: 2050.25,
    })];

    try {
        const revenueRows = await nicraReportRepository.getNicraRevenueGeneratedData(1);
        const farmRows = await nicraReportRepository.getNicraFarmImplementData(1);
        const groups = buildNicraFarmImplementGroups(farmRows);

        assert.equal(revenueRows[0].revenueGeneratedRs, 12500.75);
        assert.equal(groups.groups[0].rows[0].farmImplementUsedHours, 5.5);
        assert.equal(groups.groups[0].rows[0].revenueGeneratedRs, 2050.25);
    } finally {
        prisma.nicraRevenueGenerated.findMany = originalRevenueFindMany;
        prisma.nicraFarmImplement.findMany = originalFarmFindMany;
    }
});
